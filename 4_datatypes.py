# Data types in python

"""
Text Type:	str
Numeric Types:	int, float, complex
Sequence Types:	list, tuple, range
Mapping Type:	dict
Set Types:	set, frozenset
Boolean Type:	bool
Binary Types:	bytes, bytearray, memoryview
None Type:	NoneType

"""

a = "Sujan"                                #String
b = 5                                      #Int
c = 5.5                                    #Float
d = 1j                                     #Complex
e = ["Sujan","Suman"]                      #List  
f = ("Sujan","Suman")                      #Tuple
g = range(1,6)                             #range
h = {"Name":"Sujan","Age":34}              #dict
i = {"Sujan","Suman"}                      #Set
j = frozenset({"Sujan","Suman"})           #Frozenset
k = True                                   #bool
l = b"Sujan"                               #binary
m = bytearray(9)                           #bytearray
n = memoryview(bytes(9))                   #memoryview
o = None                                   #No type


print(a,type(a))
print(b,type(b))
print(c,type(c))
print(d,type(d))
print(e,type(e))
print(f,type(f))
print(g,type(g))
print(h,type(h))
print(i,type(i))
print(j,type(j))
print(k,type(k))
print(l,type(l))
print(m,type(m))
print(n,type(n))
print(o,type(o))








